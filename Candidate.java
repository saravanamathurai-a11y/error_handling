public class Candidate {
    private String name;
    private String gender;
    private int pge;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGender() {
        return gender;        
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public double getPge() {
        return pge;
    }

    public void setPge(int pge) {
        this.pge = pge;
    }
}